
CREATE TABLE Students(
LastName		NVARCHAR(64) NOT NULL,	
FirstName		NVARCHAR(64) NOT NULL,
NetId			NVARCHAR(64) PRIMARY KEY
);


CREATE TABLE Courses(
Dept		NVARCHAR(4) NOT NULL,
CourseNum	SMALLINT NOT NULL,
Semester	NVARCHAR(6) NOT NULL,
Year		SMALLINT NOT NULL,
CRN 		INT PRIMARY KEY NOT NULL,
Type		NVARCHAR(7) NOT NULL,
Day			NVARCHAR(5) NOT NULL,
MeetingTime NVARCHAR(64) NOT NULL,
ClassSize	SMALLINT NOT NULL
);

CREATE TABLE Enrolled(
EID			INT IDENTITY (1,1) PRIMARY KEY,
NetId		NVARCHAR(64)  FOREIGN KEY REFERENCES Students(NetId),
CRN 		INT FOREIGN KEY REFERENCES Courses(CRN)
);

CREATE TABLE Waitlist(
WID			INT IDENTITY (1,1) PRIMARY KEY,
NetId		NVARCHAR(64)  FOREIGN KEY REFERENCES Students(NetId),
CRN 		INT FOREIGN KEY REFERENCES Courses(CRN)
);
